def add(n1, n2):
    return n1 + n2
def subtract(n1,n2):
    return n1-n2
def multiply(n1,n2):
    return n1*n2
def divide(n1,n2):
    return n1/n2

operations={
    "+":add,
    "-":subtract,
    "*":multiply,
    "/":divide,
}

while True:
    print(logo)
    print("Welcome to the Python Calculator!")
    number_1=int(input("Enter the first number: "))
    should_continue=True
    while should_continue:
        operator=input("Choose an operation from '+','-','*','/': ")
        number_2=int(input("Enter the second number: "))
        result=operations[operator](number_1,number_2)
        print(f"The answer is: {result}")
        should_continue=input("Do you want to continue with the result of the previous operation? Type 'yes' or 'no': ").lower()
        if should_continue=='yes':
            number_1=result
        else:
            should_continue=False
            print("\n"*50)
